// Records the intrinsic dimensions of the manual's images into a generated TS
// file so the in-app Img component can reserve space and avoid layout shift.
//
// Run via `npm run gen:image-dims` (chained into `build`, alongside the plugin
// generators). Re-run whenever an image is added or replaced.

import { readdirSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { imageSize } from "image-size";

const here = dirname(fileURLToPath(import.meta.url));
const imgDir = join(here, "..", "..", "img", "manual");
const outFile = join(here, "..", "src", "components", "dialogs", "manual-img-dims.gen.ts");

const dims = {};
if (existsSync(imgDir)) {
	for (const name of readdirSync(imgDir).sort()) {
		if (!/\.(png|jpe?g|gif|webp)$/i.test(name)) continue;
		const { width, height } = imageSize(readFileSync(join(imgDir, name)));
		if (width && height) dims[name] = { w: width, h: height };
	}
}

const entries = Object.entries(dims)
	.map(([name, d]) => `\t${JSON.stringify(name)}: { w: ${d.w}, h: ${d.h} },`)
	.join("\n");

const out = `// AUTO-GENERATED by gen:image-dims. Do not edit by hand.
// Intrinsic dimensions of manual images (fetched from GitHub at runtime) so the
// Img component can reserve space and avoid layout shift.
export const MANUAL_IMG_DIMS: Record<string, { w: number; h: number }> = {${
	entries ? `\n${entries}\n` : ""
}};
`;

writeFileSync(outFile, out);
console.log(`[gen:image-dims] wrote ${Object.keys(dims).length} image dimension(s) to ${outFile}`);
